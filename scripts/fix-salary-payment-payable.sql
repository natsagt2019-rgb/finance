-- Банкаар цалин ОЛГОХ нь зөвхөн ӨГЛӨГийг хаана: Дт 310201 / Кт банк.
-- Тиймээс банкны гүйлгээ 700101 (зардал)-д ШУУД орох ёсгүй — давхар зардал болно.
--   node scripts/apply-sql.mjs scripts/fix-salary-payment-payable.sql
-- ============================================================

UPDATE transactions
SET debit_code = '310201'
WHERE debit_code = '700101'
  AND expense IS NOT NULL
  AND expense > 0;
