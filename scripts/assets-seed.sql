-- ============================================================
-- Үндсэн хөрөнгийн модуль — анхдагч ангиллын seed
-- ============================================================
-- assets-schema.sql дараа ажиллуулна. Ашиглалтын хугацаа нь ААНОАТ
-- хуулийн §18-ийн элэгдлийн бүлгийн дагуу (засаж болно).
-- Дахин ажиллуулахад давхцахгүй (code-оор шалгана).
--
-- Дансны уялдаа (accounts-standard-seed.sql):
--   160100 Барилга байгууламж
--   160200 Тоног төхөөрөмж, машин механизм
--   160300 Тээврийн хэрэгсэл
--   160400 Тавилга, эд хогшил
--   160500 Компьютер, биет бус хөрөнгө
--   160600 Бусад үндсэн хөрөнгө
--   160800 Хуримтлагдсан элэгдэл (хасах контр данс)
--   720300 Элэгдлийн зардал
-- ============================================================

INSERT INTO asset_categories (code, name, useful_life_years, account_code, accum_account_code, expense_account_code)
SELECT v.code, v.name, v.life, v.acc, v.accum, v.exp
FROM (VALUES
    ('ҮХ-1', 'Барилга байгууламж',                            40,  '160100', '160800', '720300'),
    ('ҮХ-2', 'Машин, тоног төхөөрөмж',                        10,  '160200', '160800', '720300'),
    ('ҮХ-3', 'Тээврийн хэрэгсэл',                             10,  '160300', '160800', '720300'),
    ('ҮХ-4', 'Тавилга, эд хогшил',                            10,  '160400', '160800', '720300'),
    ('ҮХ-5', 'Компьютер, дагалдах хэрэгсэл, програм хангамж',  3,  '160500', '160800', '720300'),
    ('ҮХ-6', 'Бусад үндсэн хөрөнгө',                          10,  '160600', '160800', '720300')
) AS v(code, name, life, acc, accum, exp)
WHERE NOT EXISTS (
    SELECT 1 FROM asset_categories c WHERE c.code = v.code
);

-- Аль хэдийн үүссэн ангиллын дансны кодыг шинэчлэх (хуучин 2xxx → 16xxxx).
UPDATE asset_categories SET
  account_code         = CASE code
    WHEN 'ҮХ-1' THEN '160100'
    WHEN 'ҮХ-2' THEN '160200'
    WHEN 'ҮХ-3' THEN '160300'
    WHEN 'ҮХ-4' THEN '160400'
    WHEN 'ҮХ-5' THEN '160500'
    WHEN 'ҮХ-6' THEN '160600'
    ELSE account_code
  END,
  accum_account_code   = '160800',
  expense_account_code = '720300'
WHERE code IN ('ҮХ-1','ҮХ-2','ҮХ-3','ҮХ-4','ҮХ-5','ҮХ-6')
  AND (account_code NOT LIKE '16%' OR accum_account_code IS NULL OR expense_account_code IS NULL);
